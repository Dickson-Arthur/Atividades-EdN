valorA = int(input("Digite o valor de A: "))
valorB = int(input("Digite o valor de B: "))
valorC = int(input("Digite o valor de C: "))
valorD = int(input("Digite o valor de D: "))

diferenca = (valorA*valorB) - (valorC*valorD)

print("A fórmula para a diferança é: (valorA*valorB) - (valorC*valorD)")
print(f"Substituindo os valores fornecidos: ({valorA} * {valorB}) - ({valorC} * {valorD})")
print(f"O valor da diferenca é: {diferenca}")
